#include <unistd.h>

void	ft_print_str(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	ft_print_str(argv[0]);
	ft_print_str("\n");
	argc = 0;
	return (argc);
}
